CREATE TABLE PRODUSE (
Id_prod NUMBER(7) PRIMARY KEY,
Nume VARCHAR2(40) NOT NULL,
Descriere VARCHAR2(80));

CREATE TABLE FURNIZORI (
Id_furn NUMBER(7) PRIMARY KEY,
Nume VARCHAR2(20),
Adresa VARCHAR2(60),
Tel NUMBER(11),
Email VARCHAR2(30),
SiTE VARCHAR2(30),
Oras VARCHAR2(20));

CREATE  TABLE CLIENTI_FIDELI (
Id_client NUMBER(7) PRIMARY KEY,
Nume VARCHAR2(50),
Prenume VARCHAR2(50),
Adresa VARCHAR2(70),
Tel NUMBER(11),
Email VARCHAR2(30));

CREATE TABLE DEPOZITE (
Id_dep NUMBER(7) PRIMARY KEY,
Nume VARCHAR2(30),
Adresa VARCHAR2(70),
Tel NUMBER(11),
Email VARCHAR2(30),
Sitee VARCHAR2(30));

CREATE TABLE DEPOZITE_PRODUSE(
Id_dp NUMBER (7) PRIMARY KEY,
Id_dep NUMBER(7),
Id_prod NUMBER(7),
FOREIGN KEY (Id_dep) REFERENCES DEPOZITE (Id_dep) on delete set null,
FOREIGN KEY (Id_prod) REFERENCES PRODUSE (Id_prod) on delete set null
);

CREATE TABLE FURNIZORI_PRODUSE(
Id_fp NUMBER (7) PRIMARY KEY,
Id_furn NUMBER(7),
Id_prod NUMBER(7),
FOREIGN KEY (Id_furn) REFERENCES FURNIZORI (Id_furn) on delete set null,
FOREIGN KEY (Id_prod) REFERENCES PRODUSE (Id_prod) on delete set null
);

create table FARMACI (
Id_farm number(7) PRIMARY KEY,
Nume varchar2(40),
Adresa varchar2 (70),
Sitee varchar2(30),
Id_manager number(7),
Telefon number(11),
Email varchar2 (30) );

CREATE TABLE ANGAJATI (
Id_ang NUMBER(7) PRIMARY KEY,
Nume varchar2(30),
Prenume varchar2 (20),
Data_nast date,
Functie varchar2(20),
Salariu number(7),
Data_angaj date,
Id_farm number(7),
Adresa varchar2(60),
Tel number(11),
Email varchar(50),
FOREIGN KEY (Id_farm) REFERENCES FARMACI (Id_farm) on delete set null
);

create table EVIDENTA_APROVIZIONARE (
Id_evap number(7) PRIMARY KEY,
Data_evap date,
Id_prod number(7),
Id_furn number(7),
Id_dep number(7),
Pret_cump number(30),
Nr_factura number(30),
Cantitate number(20),
Id_ang_resp number(7),
FOREIGN KEY (Id_prod) REFERENCES PRODUSE (Id_prod) on delete set null,
FOREIGN KEY (Id_furn) REFERENCES FURNIZORI (Id_furn) on delete set null,
FOREIGN KEY (Id_dep) REFERENCES DEPOZITE (Id_dep) on delete set null
);

create table EVIDENTA_VANZARII (
Id_evvanz number(7) PRIMARY KEY,
Data_evvanz date,
Id_client number(7),
Nr_factura number(30),
Id_prod number(7),
Pret_vanz number(30),
Discont number(20),
Cantitate number(20),
Id_ang_resp number(7),
FOREIGN KEY (Id_client) REFERENCES CLIENTI_FIDELI (Id_client) on delete set null,
FOREIGN KEY (Id_prod) REFERENCES PRODUSE (Id_prod) on delete set null
);

CREATE TABLE PROGRAM_LUCRU (
Id_prl number(7) PRIMARY KEY,
Data_prl date,
Tura number(7),
Id_ang number(7),
FOREIGN KEY (Id_ang) REFERENCES ANGAJATI (Id_ang) on delete set null
);
